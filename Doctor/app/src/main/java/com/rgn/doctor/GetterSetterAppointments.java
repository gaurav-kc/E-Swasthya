public class GetterSetterAppointments {
    public int Pat_id;
    public String Pat_name;
    public String Pat_app_date;
    public String Pat_ss;
    public String Pat_fcm;
    public int Pat_app_hr;
    public int Pat_app_min;

    public int getPat_id() {
        return Pat_id;
    }

    public void setPat_id(int pat_id) {
        Pat_id = pat_id;
    }

    public String getPat_name() {
        return Pat_name;
    }

    public void setPat_name(String pat_name) {
        Pat_name = pat_name;
    }

    public String getPat_app_date() {
        return Pat_app_date;
    }

    public void setPat_app_date(String pat_app_date) {
        Pat_app_date = pat_app_date;
    }

    public String getPat_ss() {
        return Pat_ss;
    }

    public void setPat_ss(String pat_ss) {
        Pat_ss = pat_ss;
    }

    public String getPat_fcm() {
        return Pat_fcm;
    }

    public void setPat_fcm(String pat_fcm) {
        Pat_fcm = pat_fcm;
    }

    public int getPat_app_hr() {
        return Pat_app_hr;
    }

    public void setPat_app_hr(int pat_app_hr) {
        Pat_app_hr = pat_app_hr;
    }

    public int getPat_app_min() {
        return Pat_app_min;
    }

    public void setPat_app_min(int pat_app_min) {
        Pat_app_min = pat_app_min;
    }
}
